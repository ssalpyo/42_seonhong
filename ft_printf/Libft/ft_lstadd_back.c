#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list *temp;

	if (*lst == NULL)
		*lst = new;
	else if ((temp = ft_lstlast(*lst)) != NULL)
		temp->next = new;
}
